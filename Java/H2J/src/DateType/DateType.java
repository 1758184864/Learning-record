package DateType;

public class DateType {
    public static void main(String[] args) {
        //整型
        byte b = 1;
        short s = 200;
        int i = 300;
        long l = 400;

        //字符型
        char c = '中';

        //浮点型
        double d = 123.45;
        float f2 = 54.321f;

        //布尔型,实际是以0和1的形式存于内存中，赋值只能用true和false
        boolean b1 = true;
        boolean b2 = false;

        //字符串
        String str = "Hello Java";
    }
}
